using UnityEngine;
using System.Collections;

public class Warning_Reticle_Rotation : MonoBehaviour {
	
	////////////////////////////////////////////////////
	//
	//		Constants
	//
	////////////////////////////////////////////////////

	public static readonly float ROTATION_SPEED = 180.00F;
	
	////////////////////////////////////////////////////
	//
	//		Script variables
	//
	////////////////////////////////////////////////////

	public float remaining_timer = 10.00F;

	////////////////////////////////////////////////////
	//
	//		MonoBehaviour functions
	//
	////////////////////////////////////////////////////
	
	public void Start () 	{
		
	}
	
	public void Update () 	{

		/*
		 * Rotate the reticle
		 * */
		transform.Rotate(Vector3.up, ROTATION_SPEED * Time.deltaTime, Space.Self);

		/*
		 * Decrement the timer
		 * */
		remaining_timer = remaining_timer - Time.deltaTime;

		/*
		 * Destroy the object if the timer 
		 * has expired
		 * */
		if(remaining_timer <= 0.00F)
		{
			GameObject.Destroy(this.gameObject);
		}
	}
	
	////////////////////////////////////////////////////
	//
	//		Custom functions for other scripts
	//
	////////////////////////////////////////////////////
	
	////////////////////////////////////////////////////
	//
	//		Functions available via inheritance
	//
	////////////////////////////////////////////////////
	
	////////////////////////////////////////////////////
	//
	//		Private functions for this script
	//
	////////////////////////////////////////////////////
}
